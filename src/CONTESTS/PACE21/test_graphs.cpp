VVI CE_test_graphs::createVVIfromVPII(VPII &edges) {
    int N = 0;
    for(auto e : edges) N = max(N, max(e.first, e.second));
    VVI V(N+1);
    for(auto [a,b] : edges) GraphUtils::addEdge(V,a,b);
    return V;
}

